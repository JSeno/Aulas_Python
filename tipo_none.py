"""
Tipo None
O tipo de dado None em Python, representa do tipo sem tipo, ou poderia ser conhecido também como
tipo vazio, porém falar que é um tipo sem tipo é mais apropriado.
Obs: o tipo None é sempre especificado com a primeira letra maiuscula.

Podemos utilizar None quando queremos criar uma variavel e inicializa-la com um tipo sem tipo,
antes de recebermos um valor final.

Obs: O tipo None em python é sempre considerado falso
"""
numeros = None
print(numeros)
print(type(numeros))

numeros = 1.44, 1.34, 5.67
print(numeros)
print(type(numeros))